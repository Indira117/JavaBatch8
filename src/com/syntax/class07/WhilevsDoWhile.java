package com.syntax.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		int a=20;
		
		//first check condition
		while (a<15) { 
			System.out.println("Hello");
			a++;
		} 
		System.out.println("---------------");
	int b=20;
	//first do smth
	
	do {System.out.println("Hi");
	b++;} while (b<15);
	
	
	
	
	}
	
}
